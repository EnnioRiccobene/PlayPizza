<p>
Il sito di Play Pizza utilizza cookie e tecnologie simili per garantire il corretto funzionamento delle procedure e migliorare l'esperienza di uso delle applicazioni online. Il presente documento fornisce informazioni dettagliate sull'uso dei cookie e di tecnologie similari, su come sono utilizzati e su come gestirli.
</p>

<h3>Definizioni</h3>

<p>
I cookie sono brevi frammenti di testo (lettere e/o numeri) che permettono al server web di memorizzare sul client (il browser) informazioni da riutilizzare nel corso della medesima visita al sito (cookie di sessione) o in seguito, anche a distanza di giorni (cookie persistenti). I cookie vengono memorizzati, in base alle preferenze dell'utente, dal singolo browser sullo specifico dispositivo utilizzato (computer, tablet, smartphone).

Tecnologie similari, come, ad esempio, web beacon, GIF trasparenti e tutte le forme di storage locale introdotte con HTML5, sono utilizzabili per raccogliere informazioni sul comportamento dell'utente e sull'utilizzo dei servizi.

Nel seguito di questo documento faremo riferimento ai cookie e a tutte le tecnologie similari utilizzando semplicemente il termine “cookie”.
</p>

<h3>Tipologie di cookie</h3>

<p>In base alle caratteristiche e all'utilizzo dei cookie possiamo distinguere diverse categorie:

Cookie strettamente necessari. Si tratta di cookie indispensabili per il corretto funzionamento di www.ristorantemargarita.it e sono utilizzati per gestire il login e l'accesso alle funzioni riservate del sito. La durata dei cookie è normalmente limitata alla sessione di lavoro (chiuso il browser solitamente vengono cancellati) anche se in alcuni casi possono permanere alcuni cookies.
Cookie di analisi e prestazioni. Sono cookie utilizzati per raccogliere e analizzare il traffico e l'utilizzo del sito in modo anonimo. Questi cookie, pur senza identificare l'utente, consentono, per esempio, di rilevare se il medesimo utente torna a collegarsi in momenti diversi. Permettono inoltre di monitorare il sistema e migliorarne le prestazioni e l'usabilità. La disattivazione di tali cookie può essere eseguita senza alcuna perdita di funzionalità.
Cookie di profilazione. Si tratta di cookie permanenti utilizzati per identificare (in modo anonimo e non) le preferenze dell'utente e migliorare la sua esperienza di navigazione.
Cookie di terze parti

Visitando un sito web si possono ricevere cookie sia dal sito visitato (“proprietari”), sia da siti gestiti da altre organizzazioni (“terze parti”). Un esempio notevole è rappresentato dalla presenza dei “social plugin” per Facebook, Twitter, Google+ e LinkedIn. Si tratta di parti della pagina visitata generate direttamente dai suddetti siti ed integrati nella pagina del sito ospitante. L'utilizzo più comune dei social plugin è finalizzato alla condivisione dei contenuti sui social network.

La presenza di questi plugin comporta la trasmissione di cookie da e verso tutti i siti gestiti da terze parti. La gestione delle informazioni raccolte da “terze parti” è  disciplinata dalle relative informative cui si prega di fare riferimento.
</p>

<h3>Durata dei cookie</h3>

<p> cookie (cookie di sessione) restano attivi solo fino alla chiusura del browser o all'esecuzione del comando di logout. Altri cookie “sopravvivono” alla chiusura del browser e sono disponibili anche in successive visite dell'utente.

Questi cookie sono detti persistenti e la loro durata è fissata dal server al momento della loro creazione. In alcuni casi è fissata una scadenza, in altri casi la durata è illimitata.

www.ristorantemargarita.it non fa uso di cookie persistenti. Tuttavia, navigando sulle pagine del sito web si può interagire con siti gestiti da terze parti che possono creare o modificare cookie permanenti e di profilazione.
</p>
<h3>Gestione dei cookie</h3>

<p>L'utente può decidere se accettare o meno i cookie utilizzando le impostazioni del proprio browser.

Attenzione: la disabilitazione totale o parziale dei cookie tecnici può compromettere l'utilizzo delle funzionalità del sito riservate agli utenti registrati. Al contrario, la fruibilità dei contenuti pubblici è possibile anche disabilitando completamente i cookie.

La disabilitazione dei cookie “terze parti” non pregiudica in alcun modo la navigabilità.

L'impostazione può essere definita in modo specifico per i diversi siti e applicazioni web. Inoltre i migliori browser consentono di definire impostazioni diverse per i cookie “proprietari” e per quelli di “terze parti”.

A titolo di esempio, in Firefox, attraverso il menu Strumenti->Opzioni ->Privacy, è possibile accedere ad un pannello di controllo dove è possibile definire se accettare o meno i diversi tipi di cookie e procedere alla loro rimozione.	
</p>
		
